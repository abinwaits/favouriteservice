package com.stackroute.favouriteservice.dto;

/**
 * @author ubuntu
 *
 */
public class ParticipantDetails {
	private String name;
	private String deathType;
	private String vehicleDestroys;
	private String weaponsAcquired;
	private String winPlace;
	private String killPlace;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDeathType() {
		return deathType;
	}
	public void setDeathType(String deathType) {
		this.deathType = deathType;
	}
	public String getVehicleDestroys() {
		return vehicleDestroys;
	}
	public void setVehicleDestroys(String vehicleDestroys) {
		this.vehicleDestroys = vehicleDestroys;
	}
	public String getWeaponsAcquired() {
		return weaponsAcquired;
	}
	public void setWeaponsAcquired(String weaponsAcquired) {
		this.weaponsAcquired = weaponsAcquired;
	}
	public String getWinPlace() {
		return winPlace;
	}
	public void setWinPlace(String winPlace) {
		this.winPlace = winPlace;
	}
	public String getKillPlace() {
		return killPlace;
	}
	public void setKillPlace(String killPlace) {
		this.killPlace = killPlace;
	}
	
	
}
